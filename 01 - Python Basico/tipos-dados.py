# Python é uma linguagem
# Os tipos mais comuns são os numéricos
# Há os tipos int que são para números inteiros postivos ou negativos:
inteiro = 1
# Podemos usar a função type para verificar o tipo de uma variável
print(type(inteiro))
# Há o tipo ponto fluente, que são números que possuem valor após o ponto positivos ou negativos:
flutuante = 3.4
print(type(flutuante))